// Виконує локальну логіку parse stone size для модуля catalog-products.
function parseStoneSize(value) {
  const normalized = String(value || "").trim().toLowerCase();
  const number = Number.parseFloat(normalized.replace(",", "."));
  if (Number.isNaN(number)) return 0;
  if (normalized.includes("ct")) return number * 1200;
  if (normalized.includes("mm")) return number * 180;
  return number * 260;
}

// Обчислює round price та повертає стабільний результат для бізнес-логіки.
function roundPrice(value) {
  return Math.round(value / 10) * 10;
}

// Обчислює compute price та повертає стабільний результат для бізнес-логіки.
function computePrice(definition) {
  const base = TYPE_BASE_PRICE[definition.type] || 3500;
  const metal = METAL_PRICE_PREMIUM[definition.metal] || 0;
  const stone = STONE_PRICE_PREMIUM[definition.stoneType] || 700;
  const size = parseStoneSize(definition.stoneSize);
  const style = STYLE_PRICE_PREMIUM[definition.visualStyle] || 0;
  return roundPrice(base + metal + stone + size + style);
}

// Створює новий запис або чернетку для create product.
function createProduct(definition) {
  const typeMeta = TYPE_LABELS[definition.type];
  const metalMeta = METAL_COPY[definition.metal];
  const name_uk = `${typeMeta.uk} ${definition.title_uk}`;
  const name_en = `${definition.title_en} ${typeMeta.en}`;

  return {
    ...definition,
    name_uk,
    name_en,
    description_uk:
      `${name_uk} — ${definition.story_uk}. Виріб виконаний у ${metalMeta.uk} ` +
      `з акцентом ${definition.stone_descriptor_uk} та зберігає виразну, але стриману подачу для щоденного стилю й особливих подій.`,
    description_en:
      `${name_en} is ${definition.story_en}. Crafted in ${metalMeta.en}, it is finished with ` +
      `${definition.stone_descriptor_en} and keeps a refined balance between everyday elegance and occasion dressing.`,
    filter_type: typeMeta.filter,
    price: computePrice(definition)
  };
}

